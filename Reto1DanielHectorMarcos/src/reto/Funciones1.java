package reto;

public class Funciones1 {
public static boolean esPositivo(int n) {
	if(n>0) {
		return true;
	}else {
		return false;
	}
}
public static boolean esNegativo(int n) {
	if(n<0) {
		return true;
	}else {
		return false;
	}
}
public static int multiplicacion(int n1,int n2) {
	return n1*n2;
}
public static int division(int n1,int n2) {
	return n1/n2;
}
}
